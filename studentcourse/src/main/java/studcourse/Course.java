package studcourse;

public class Course {

    private String oop;
    private int oopEAP;
    private String linearAlgebra;
    private int linearAlgebraEAP;
    private String automationTesting;
    private int automationTestingEAP;

    public void setOop(String newOop) {
        oop = newOop;
    }

    public String getOop() {
        return oop;
    }

    public void setOopEAP(int newOopEAP) {
        oopEAP = newOopEAP;
    }

    public int getOopEAP() {
        return oopEAP;
    }

    public void setLinearAlgebra(String newLinearAlgebra) {
        linearAlgebra = newLinearAlgebra;
    }

    public String getLinearAlgebra() {
        return linearAlgebra;
    }

    public void setLinearAlgebraEAP(int newLinearAlgebraEAP) {
        linearAlgebraEAP = newLinearAlgebraEAP;
    }

    public int getLinearAlgebraEAP() {
        return linearAlgebraEAP;
    }

    public void setAutomationTesting(String newAutomationTesting) {
        automationTesting = newAutomationTesting;
    }

    public String getAutomationTesting() {
        return automationTesting;
    }

    public void setAutomationTestingEAP(int newAutomationTestingEAP) {
        automationTestingEAP = newAutomationTestingEAP;
    }

    public int getAutomationTestingEAP() {
        return automationTestingEAP;
    }
}

